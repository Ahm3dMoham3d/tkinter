# simple for loop
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)

# The break Statement
for x in fruits:
    print(x)
    if x == "banana":
        break

# The continue Statement
for x in fruits:
    if x == "banana":
        continue
    print(x)

# range function
for x in range(6):
    print(x)


